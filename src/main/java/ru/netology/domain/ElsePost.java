package ru.netology.domain;

public class ElsePost {
    String appeal;
    String subscribeOnComments;
    String saveToMarker;

    public String getAppeal() {
        return appeal;
    }

    public void setAppeal(String appeal) {
        this.appeal = appeal;
    }

    public String getSubscribeOnComments() {
        return subscribeOnComments;
    }

    public void setSubscribeOnComments(String subscribeOnComments) {
        this.subscribeOnComments = subscribeOnComments;
    }

    public String getSaveToMarker() {
        return saveToMarker;
    }

    public void setSaveToMarker(String saveToMarker) {
        this.saveToMarker = saveToMarker;
    }
}
